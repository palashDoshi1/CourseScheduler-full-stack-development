    private void ScheduleClass_Submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleClass_Submit_ButtonActionPerformed
        String courseCode = (String) ScheduleClass_SelectClass_ComboBox.getSelectedItem();
        StudentEntry student = (StudentEntry) ScheduleClass_SelectStudent_ComboBox.getSelectedItem();

        if (courseCode == null || student == null) {
            ScheduleClass_DisplayClassStudent_Label.setText("Please select both class and student.");
            return;
        }

        // how many can sit
        int seats = ClassQueries.getClassSeats(currentSemester, courseCode);
        // how many already scheduled (status = 'S')
        int scheduledCount = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);

        String status;
        if (scheduledCount < seats) {
            status = "S";  // scheduled
        } else {
            status = "W";  // waitlisted
        }

        ScheduleEntry entry = new ScheduleEntry(
            currentSemester,
            courseCode,
            student.getStudentID(),
            status,
            new java.sql.Timestamp(System.currentTimeMillis())
        );

        ScheduleQueries.addScheduleEntry(entry);

        if (status.equals("S")) {
            ScheduleClass_DisplayClassStudent_Label.setText(
                student.toString() + " has been scheduled for the class");
        } else {
            ScheduleClass_DisplayClassStudent_Label.setText(
                student.toString() + " has been waitlisted for the class");
        }
    }//GEN-LAST:event_ScheduleClass_Submit_ButtonActionPerformed

