    private void Admin_DropStudent_Submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Admin_DropStudent_Submit_ButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry toDrop = (StudentEntry) Admin_DropStudent_ChooseStudent_ComboBox.getSelectedItem();
        if (toDrop == null) return;

        String studentID = toDrop.getStudentID();
        String fullName = toDrop.getLastName() + ", " + toDrop.getFirstName() + " " + studentID;

        StringBuilder sb = new StringBuilder();
        sb.append(fullName).append(" has been dropped from the list of students.\n\n");

        // Get all semesters where this student appears in schedule
        ArrayList<String> semesters = ScheduleQueries.getSemestersForStudent(studentID);

        for (String sem : semesters) {
            sb.append("For Semester: ").append(sem).append("\n");

            // All schedule entries for this student in this semester
            ArrayList<ScheduleEntry> entries =
                ScheduleQueries.getScheduleByStudent(sem, studentID);

            for (ScheduleEntry se : entries) {
                String course = se.getCourseCode();

                if (se.getStatus().equals("S")) {
                    // They were scheduled
                    sb.append(fullName).append(" has been dropped from ").append(course).append("\n");

                    // Delete this row
                    ScheduleQueries.deleteScheduleEntry(sem, course, studentID);

                    // Promote first waitlisted student if any
                    ArrayList<ScheduleEntry> waitList =
                        ScheduleQueries.getWaitlistedStudentsByClass(sem, course);
                    if (!waitList.isEmpty()) {
                        ScheduleEntry promoted = waitList.get(0);
                        ScheduleQueries.updateStatus(sem, course, promoted.getStudentID(), "S");

                        StudentEntry promotedStudent =
                            StudentQueries.getStudent(promoted.getStudentID());
                        if (promotedStudent != null) {
                            String promoName = promotedStudent.getLastName() + ", " +
                                               promotedStudent.getFirstName() + " " +
                                               promotedStudent.getStudentID();
                            sb.append(promoName).append(" has been scheduled into ")
                              .append(course).append("\n");
                        }
                    }
                } else { // they were waitlisted
                    sb.append(fullName).append(" has been dropped from the waitlist for ")
                      .append(course).append("\n");
                    ScheduleQueries.deleteScheduleEntry(sem, course, studentID);
                }
            }
            sb.append("\n");
        }

        // Finally, remove from student table
        StudentQueries.deleteStudent(studentID);

        // Refresh combo boxes
        rebuildStudentComboBoxes();

        Admin_DropStudent_TextArea.setText(sb.toString());
    }//GEN-LAST:event_Admin_DropStudent_Submit_ButtonActionPerformed

    private void Admin_DropClass_Submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Admin_DropClass_Submit_ButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = (String) Admin_DropClass_ChoseClass_ComboBox.getSelectedItem();
        if (courseCode == null) return;

        StringBuilder sb = new StringBuilder();

        // All scheduled & waitlisted before deleting
        ArrayList<ScheduleEntry> scheduled =
            ScheduleQueries.getScheduledStudentsByClass(currentSemester, courseCode);
        ArrayList<ScheduleEntry> waitlisted =
            ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, courseCode);

        sb.append("Scheduled students dropped from the course:\n");
        for (ScheduleEntry se : scheduled) {
            StudentEntry s = StudentQueries.getStudent(se.getStudentID());
            if (s != null) {
                sb.append(s.getLastName()).append(",").append(s.getFirstName())
                  .append(" ").append(s.getStudentID()).append("\n");
            }
        }
        sb.append("\nWaitlisted students dropped from the course:\n");
        for (ScheduleEntry se : waitlisted) {
            StudentEntry s = StudentQueries.getStudent(se.getStudentID());
            if (s != null) {
                sb.append(s.getLastName()).append(",").append(s.getFirstName())
                  .append(" ").append(s.getStudentID()).append("\n");
            }
        }

        // Delete schedule rows and class row
        ScheduleQueries.deleteScheduleByClass(currentSemester, courseCode);
        ClassQueries.dropClass(currentSemester, courseCode);

        // Refresh all class-related combos
        rebuildClassComboBoxes();

        Admin_DropClass_TextArea.setText(sb.toString());
    }//GEN-LAST:event_Admin_DropClass_Submit_ButtonActionPerformed

    private void Student_DropClass_Submit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Student_DropClass_Submit_ButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry student = (StudentEntry) Student_DropClass_ChooseStudent_ComboBox.getSelectedItem();
        String courseCode = (String) Student_DropClass_ChooseClass_ComboBox.getSelectedItem();

        if (student == null || courseCode == null) {
            Student_DropClass_TextArea.setText("Please choose both student and class.");
            return;
        }

        String studentID = student.getStudentID();
        String name = student.getLastName() + ", " + student.getFirstName() + " " + studentID;

        // Find their schedule entry (S or W) for this class
        ArrayList<ScheduleEntry> entries =
            ScheduleQueries.getScheduleByStudent(currentSemester, studentID);

        ScheduleEntry target = null;
        for (ScheduleEntry se : entries) {
            if (se.getCourseCode().equals(courseCode)) {
                target = se;
                break;
            }
        }

        if (target == null) {
            Student_DropClass_TextArea.setText("Student is not in that class this semester.");
            return;
        }

        StringBuilder sb = new StringBuilder();

        // If they were scheduled – promote first waitlist
        if (target.getStatus().equals("S")) {
            ScheduleQueries.deleteScheduleEntry(currentSemester, courseCode, studentID);
            sb.append(name).append(" has been dropped from ").append(courseCode).append("\n");

            ArrayList<ScheduleEntry> waitList =
                ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, courseCode);
            if (!waitList.isEmpty()) {
                ScheduleEntry promoted = waitList.get(0);
                ScheduleQueries.updateStatus(currentSemester, courseCode, promoted.getStudentID(), "S");

                StudentEntry promotedStudent = StudentQueries.getStudent(promoted.getStudentID());
                if (promotedStudent != null) {
                    String promoName = promotedStudent.getLastName() + ", " +
                                       promotedStudent.getFirstName() + " " +
                                       promotedStudent.getStudentID();
                    sb.append(promoName).append(" has been scheduled into ")
                      .append(courseCode).append("\n");
                }
            }
        } else { // they were waitlisted
            ScheduleQueries.deleteScheduleEntry(currentSemester, courseCode, studentID);
            sb.append(name).append(" has been dropped from ").append(courseCode)
              .append(" waitlist\n");
        }

        Student_DropClass_TextArea.setText(sb.toString());
    }//GEN-LAST:event_Student_DropClass_Submit_ButtonActionPerformed

